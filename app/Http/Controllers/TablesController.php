    {
        $number_of_tables = $request->number_of_tables;
        Tables::truncate();
        for ($i = 0; $i < $number_of_tables; $i++) {
            Tables::insert([
                'is_available' => true,
            ]);
        }


        return redirect()->route('table.index')->with('success', 'Successful updated number ot tables and zones');

